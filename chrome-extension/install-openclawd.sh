# ═══════════════════════════════════════════════════════════════
#  OpenClawd — One-Shot Installer
#  Installs: Chrome Extension + Page Agent + MCP Bridge
#  Connects to: OpenClawd orchestrator, SolanaOS daemon
# ═══════════════════════════════════════════════════════════════

set -euo pipefail

REPO_ROOT="$(cd "$(dirname "$0")" && pwd)"
EXT_DIR="$REPO_ROOT/chrome-extension"
MCP_DIR="$REPO_ROOT/openclawd-stack/orchestrator"

# Colors
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
PURPLE='\033[0;35m'
NC='\033[0m' # No Color
